INCLUDE EMU8086.INC
.MODEL SMALL
.STACK 100H 

.DATA
TESTCASE DW ?

.CODE
MAIN PROC
    ;INITIALIZA DATA 
    ;SEGMENT
    MOV AX,@DATA
    MOV DS,AX
    
WHILE_LOOP:
    ;SCAN THE NUMBER
    CALL SCAN_NUM
    PRINTN
    MOV TESTCASE,CX
    ;IF CX==0
    CMP CX,0
    JZ EXIT
    ;ELSE
    CMP CX,100
    ;NESTED IF CX<=100
    JLE P91
    ;NESTED ELSE
    PRINT "f91 "
    MOV AX,TESTCASE
    CALL PRINT_NUM
    PRINT " = "
    SUB CX,10;
    MOV AX,CX
    CALL PRINT_NUM
    PRINTN
    JMP WHILE_LOOP
    
P91:
    PRINT "f91 "
    MOV AX,TESTCASE
    CALL PRINT_NUM
    PRINT " = 91"
    PRINTN
    JMP WHILE_LOOP
        
   
EXIT:   
    ;RETURN TO DOS
    MOV AH,4CH
    INT 21H     
         
    MAIN ENDP 


    DEFINE_SCAN_NUM
    DEFINE_PRINT_STRING
    DEFINE_PRINT_NUM
    DEFINE_PRINT_NUM_UNS
END MAIN 
